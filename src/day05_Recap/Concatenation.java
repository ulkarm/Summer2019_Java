package day05_Recap;

public class Concatenation {
	
	public static void main(String[] args) {
		
		
		
		/*
		 * String: represents the sequence of characters, used for storing text
		 *         String values are surrendered by a double quote ""
		 * 
		 * Concatenation: combining, linking things together
		 *                achieved by using +
		 * 
		 * 
		 */
		
		
		
		
	
		String str = "any text goes here";
		System.out.println(str);
		
		String str2 = "1";
		System.out.println(str2);
		
		String str3 = "Hello";
		System.out.println(str3);
	
		
		String myName = "Cybertek" + " School";
		System.out.println(myName );
		
		String Year = "This is " + 2019;
		System.out.println(Year);
		
		
		String name = "Ulkar";
		System.out.println("My name is "+ name);
		
		System.out.println("1"+1+2+3);
		System.out.println(1+2+3);
		
		//System.out.println("1"+1-3);
		
		System.out.println("Batch 12" +1+2); //"text" + 1 + 2// text12//Batch1212
		System.out.println('3'+12);
		System.out.println('7'+3);
		System.out.println('a'+3);   // 'a'+3= 97+3=100
		System.out.println('a'+'b'); // 'a'+'b'= 97+98=195
		System.out.println('a'+"b"); // "a'+"b"= ab

		System.out.println(2-1+1+"4");
		
	
	
	}

}
