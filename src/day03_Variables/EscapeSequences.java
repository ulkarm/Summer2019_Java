package day03_Variables;

public class EscapeSequences {
	
	/*
	 * \n or \r : starts from new line, n used more, r is old version
	 * \t : Horizontal tab
	 * \\ : it prints a backslash 
	 * ' or \' :prints single quote
	 * \" : prints double quote (you have to put one at the beginning, one at the end
	 */
	
	public static void main(String[] args) {
		System.out.println("Hello Cybertek\n Batch 12 Students");
		
		System.out.print("Tomorrow is off\n");
		System.out.print("True\n");
		System.out.print("False\n\n\n");
		
		System.out.println("\tClass starts tomorrow");
		
		System.out.println("\\Ulkar");
		System.out.println("'Java");
		System.out.println("\'Java");
		System.out.println("Book is called \"Game of Thrones\"");
		
		
		//
		
		
	}

}
