package day03_Variables;

public class Println_vs_Print {
	
	//the only characters we can give for any name is: _ and $
	
	public static void main(String[] args) {
		
		System.out.println("Hello Cybertek"); // println prints from a new line
		System.out.println("School");
		
		System.out.println("================");
		
		System.out.print("Hello Cybertek");
		System.out.print("School"); //printing takes place from where it's left
		System.out.println();
		
		System.out.println("================");
		
		System.out.println("Today is ");
		System.out.print("a great day");
		
		
		
	}

}
